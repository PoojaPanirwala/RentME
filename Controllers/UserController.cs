using RentME.Data;
using RentME.Models;

namespace RentME.Controllers
{
    public class UserController : Controller
    {
        private readonly UserContext _context;

        public UserController(UserContext context)
        {
            _context = context;
        }

        public IActionResult Login()
        {
            //HttpContent.Session["usertype"] ="";
            return View();
        }

        // GET: User
